export function getStoredLang(): Lang {
  if (typeof window === 'undefined') return 'de'
  return (localStorage.getItem('lang') as Lang) ?? 'de'
}

export function setStoredLang(lang: Lang) {
  localStorage.setItem('lang', lang)
}

